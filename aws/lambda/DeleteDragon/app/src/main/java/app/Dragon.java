package app;

public class Dragon {

    String description;
    String name;
    String family;
    String spottedCity;
    String spottedNeighborhood;
    String spottedState;

    public String getDescription() {
        return description;
    }

    public String getName() {
        return name;
    }

    public String getFamily() {
        return family;
    }

    public String getSpottedCity() {
        return spottedCity;
    }

    public String getSpottedNeighborhood() {
        return spottedNeighborhood;
    }

    public String getSpottedState() {
        return spottedState;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setFamily(String family) {
        this.family = family;
    }

    public void setSpottedCity(String spottedCity) {
        this.spottedCity = spottedCity;
    }

    public void setSpottedNeighborhood(String spottedNeighborhood) {
        this.spottedNeighborhood = spottedNeighborhood;
    }

    public void setSpottedState(String spottedState) {
        this.spottedState = spottedState;
    }

}
